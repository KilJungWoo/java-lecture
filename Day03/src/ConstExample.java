
public class ConstExample {

	public static void main(String[] args) {
		
		int age = 10;
		System.out.println(age);
		
		age = 20;
		System.out.println(age);
		
		final double PI = 3.14;
		System.out.println(PI);
		
		//PI = 3.20; // 상수는 변경할수 없으므로 에러
		System.out.println(PI);
		
		
		
		
		
		

	}

}
