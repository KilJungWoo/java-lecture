// 변수
// 변수 명명 규칙은 단봉낙타


public class Varisable {
	
	public static void main(String[] args) {
		
		//자료형변수명 = 데이터;
		
		int a = 10;
		int x;
	    x = 9;
	    int myAge = 19;
		
		System.out.println(a);
		System.out.println(a + a);
		System.out.println(a * a);
		
		x = a - x;
		
		// 변수의 문법적 특징
		// 1. 변수의 시작은 숫자일수 없다
		// int 0name; = error
		// int name-; = 정상
		// 2. 변수의 이름은 예약어가 될 수 없다.
		// int class; = error 자바 컴파일러가 이미 사용하고 있는 예약어
		// 3. 변수명은 공백을 허용하지 않는다             
		// 4. 같은 Scope에 같은 이름의 변수는 허용되지 않는다.
		// 5. 대소문자는 구분한다.
		
		
	}

}
